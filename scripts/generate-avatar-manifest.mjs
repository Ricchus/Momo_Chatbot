import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import {
  AVATAR_LOOP_FPS,
  AVATAR_TRANSITION_FPS,
  getLoopTargetFps,
  getTransitionTargetFps
} from "./avatar-pipeline-config.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const publicRoot = path.join(projectRoot, "public", "avatar");
const generatedReverseRoot = path.join(projectRoot, "generated", "avatar", "reverse");
const outputFile = path.join(projectRoot, "src", "features", "avatar", "avatarManifest.generated.ts");

const DEFAULT_LOOP_DURATION_MS = 5000;
const DEFAULT_TRANSITION_DURATION_MS = 2000;
const rawExpressions = new Map();

const states = [
  "idle_neutral",
  "warm_friendly",
  "listening_attentive",
  "thinking_process",
  "speaking_explain",
  "positive_happy"
];
const stateSet = new Set(states);

function toPosixPath(filePath) {
  return filePath.split(path.sep).join("/");
}

function readFrameCount(filePath) {
  const result = spawnSync(
    "ffprobe",
    [
      "-v",
      "error",
      "-select_streams",
      "v:0",
      "-count_frames",
      "-show_entries",
      "stream=nb_read_frames",
      "-of",
      "default=noprint_wrappers=1:nokey=1",
      filePath
    ],
    {
      encoding: "utf8",
      stdio: "pipe"
    }
  );

  if (result.error || result.status !== 0) {
    return null;
  }

  const value = Number.parseInt(result.stdout.trim(), 10);
  return Number.isFinite(value) && value > 0 ? value : null;
}

function durationMsFor(filePath, fallbackMs, targetFps) {
  const frameCount = readFrameCount(filePath);
  if (!frameCount) {
    return fallbackMs;
  }

  return Math.round((frameCount / targetFps) * 1000);
}

function registerRawExpression(expression) {
  const token = `__RAW_EXPR_${rawExpressions.size}__`;
  rawExpressions.set(token, expression);
  return token;
}

function getReverseSrcToken(id) {
  const reversePath = path.join(generatedReverseRoot, `${id}__rev.gif`);
  if (!fs.existsSync(reversePath)) {
    return undefined;
  }

  const relativePath = toPosixPath(path.relative(path.dirname(outputFile), reversePath));
  return registerRawExpression(`new URL(${JSON.stringify(relativePath)}, import.meta.url).href`);
}

function scanLoops() {
  const result = {};

  for (const state of states) {
    const dir = path.join(publicRoot, "loops", state);
    const files = fs.existsSync(dir)
      ? fs.readdirSync(dir).filter((file) => /\.(gif|webm)$/i.test(file)).sort()
      : [];

    result[state] = files.map((file) => {
      const filePath = path.join(dir, file);
      const assetId = file.replace(/\.(gif|webm)$/i, "");
      return {
        id: assetId,
        kind: "loop",
        state,
        src: `/avatar/loops/${state}/${file}`,
        durationMs: durationMsFor(filePath, DEFAULT_LOOP_DURATION_MS, getLoopTargetFps(assetId)),
        format: file.toLowerCase().endsWith(".webm") ? "webm" : "gif"
      };
    });
  }

  return result;
}

function scanTransitions() {
  const dir = path.join(publicRoot, "transitions");
  const files = fs.existsSync(dir)
    ? fs.readdirSync(dir).filter((file) => /\.(gif|webm)$/i.test(file)).sort()
    : [];
  const result = {};

  for (const file of files) {
    const match = file.match(/^tr_(.+)_to_(.+)\.(gif|webm)$/i);
    if (!match) continue;

    const [, from, to, ext] = match;
    if (!stateSet.has(from) || !stateSet.has(to)) {
      continue;
    }
    const id = `tr_${from}_to_${to}`;

    result[id] = {
      id,
      kind: "transition",
      from,
      to,
      src: `/avatar/transitions/${file}`,
      reverseSrc: getReverseSrcToken(id),
      durationMs: durationMsFor(path.join(dir, file), DEFAULT_TRANSITION_DURATION_MS, getTransitionTargetFps(id)),
      format: ext.toLowerCase() === "webm" ? "webm" : "gif"
    };
  }

  return result;
}

const manifest = {
  loops: scanLoops(),
  transitions: scanTransitions()
};

let manifestText = JSON.stringify(manifest, null, 2);
for (const [token, expression] of rawExpressions) {
  manifestText = manifestText.replace(JSON.stringify(token), expression);
}

const output = `/* auto-generated by scripts/generate-avatar-manifest.mjs */\n` +
  `import type { AvatarManifest } from "./types";\n\n` +
  `const manifest: AvatarManifest = ${manifestText};\n\n` +
  `export default manifest;\n`;

fs.writeFileSync(outputFile, output, "utf8");
console.log(
  `Generated manifest: ${path.relative(projectRoot, outputFile)} (default loop fps: ${AVATAR_LOOP_FPS}, default transition fps: ${AVATAR_TRANSITION_FPS})`
);
